const day = 1;
switch (day) {
  case 1:
    console.log("Sun");
    break;
  case 2:
    console.log("Mon");
    break;
  case 3:
    console.log("Tue");
    break;
  case 4:
    console.log("Wed");
    break;
  case 5:
    console.log("Thur");
    break;
  case 6:
    console.log("Fri");
    break;
  case 7:
    console.log("Sat");
    break;

  default:
    break;
}
